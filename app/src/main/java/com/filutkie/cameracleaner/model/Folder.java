package com.filutkie.cameracleaner.model;

public class Folder {

	private String name;
	private String size;

	public String getName() {
		return name;
	}

	public void setName(String title) {
		this.name = title;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

}
